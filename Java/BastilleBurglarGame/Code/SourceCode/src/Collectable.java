public class Collectable
{
	/*
	This is a collectable object, it has several variables.
	Score - this is the score that the collectable has - allows for coins which provide
			more than one score.
	Type  - this is the type of object; is it a coin or chest? Currently only allowed to
			be 0, as this is simply a coin.
	*/
	public int score = 1;
	public int type = 0;
}